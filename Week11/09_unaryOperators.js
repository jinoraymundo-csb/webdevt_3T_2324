// Increment or Decrement ++ or --
// Prefix and Postfix

// prefix increment
let age = 60;
console.log(age);
console.log(++age);
console.log(age);

// postfix increment
let amount = 40;
console.log(amount);
console.log(amount++);
console.log(amount);

// prefix decrement
let price = 100;
console.log(price);
console.log(--price);
console.log(price);

// postfix decrement
let count = 88;
console.log(count);
console.log(count--);
console.log(count);