let floatNum1 = 3.125e7;  // equal to 31250000
let floatNum2 = 3e-2;     // equal to 0.03

console.log(`The value of floatNum1 is ${floatNum1}`);
console.log(`The value of floatNum2 is ${floatNum2}`);

// 3.125e7 -> move the decimal point 7x to the right
// 31250000.0

// 3e-2 -> move the decimal point 2x to the left
// .03